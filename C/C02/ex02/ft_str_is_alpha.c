int	ft_str_is_alpha(char *str)
{
	int	i;
	int	as_num;

	i = 0;
	while (str[i] != '\0')
	{
		as_num = (int)str[i];
		if ((as_num < 65) || (as_num > 122) || (as_num > 90 && as_num < 97))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
