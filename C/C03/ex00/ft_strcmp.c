int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if ((int)(s1[i]) != (int)(s2[i]))
		{
			return ((int)(s1[i]) - (int)(s2[i]));
		}
		i++;
	}
	return (0);
}
